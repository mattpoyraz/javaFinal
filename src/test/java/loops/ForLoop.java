package loops;

public class ForLoop {
    public static void main(String []args){

        for(int i=1; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("All the numbers are printed as for loop");

        for(int x=10; x>=0; x=x-2){
            System.out.println(x);
        }
        System.out.println("all the number are printed as even numbers");

    }


}
